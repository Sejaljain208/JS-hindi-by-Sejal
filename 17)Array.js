// an array is like a row of boxes,where each box can hold a value and has an index number to identify its position in the row.

// array helps you to store multiple values in a single variable instead of creating separate variables for each value.

// creting an array
let fruits = ['apple', 'banana', 'cherry', 'date'];
console.log(fruits); // output: ['apple', 'banana', 'cherry', 'date']

// accessing array elements using index
console.log(fruits[0]); // output: apple
console.log(fruits[2]); // output: cherry

// modifying array elements
fruits[1] = 'blueberry';
console.log(fruits); // output: ['apple', 'blueberry', 'cherry', 'date']

// indexing starts from 0. we can update the value at any index position.any type of value can be stored in an array.

