// dynamic typing : 
// js is not static typing language. javascript is a dynamically typed language . iska matlab ye hai ki hum variable ko declare karte waqt uska data type nahi batate hai or hum variable ki value ko kabhi bhi change kar sakte hai or uska data type bhi change ho jata hai.

let e = 10; //number 
e = "sejal"; //string
e = true; //boolean
e = null; //null
e = []; //array
e = {}; //object
e = function() {}; //function

//humne variable e ki value ko baar baar change kiya or uska data type bhi change hota gaya. yehi hai dynamic typing ka concept.

