// reverse (): ye array ke elements ko ulta kar deta hai, matlab last element pehle aa jata hai aur pehla element last me chala jata hai.

let arr = [10, 20, 30, 40, 50];
console.log("Original Array:", arr); // output: [10, 20, 30, 40, 50]

arr.reverse();
console.log("Reversed Array:", arr); // output: [50, 40, 30, 20, 10]
